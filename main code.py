def bread1(n):
    print("\n")
    print("INGREDIENTS")
    print(2*n,"cups all-purpose flour" )
    print(1*n,"teaspoon active dry yeast")
    print(1*n,"teaspoon sugar")
    print(1/2*n,"teaspoon salt")
    print(2*n,"tablespoons yogurt")
    print(1*n,"tablespoon oil" )
    print(1/2*n,"cup lukewarm water" )
    print(2*n,"tablespoons melted butter")
    print("Fresh cilantro leaves" )
    print("\n")
    print("STEPS")
    print("1.Mix yeast, sugar, and lukewarm water. Let it sit for 5-10 mins until frothy.\n2.Combine flour, salt, yogurt, oil, and activated yeast mixture. Knead into a smooth dough.\n3.Place dough in an oiled bowl, cover, and let it rise for 1-2 hours until doubled in size.\n4. Divide dough into 6-8 balls. Roll each into a thin oval or teardrop shape.\n5.Heat a skillet over medium-high. Cook each naan for 1-2 mins on each side, pressing gently to puff.\n6.Brush cooked naan with melted butter.\n7.Optionally garnish with cilantro leaves. Serve warm with curry or dipping sauce.\n")


