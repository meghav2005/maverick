def chk_chettinad(n):
    print(red('BASIC INGREDIENTS\n', ['bold', 'underlined']))
    print("Chicken -", 500 * n, """grams, cut into pieces
Oil -""", 3 * n, """tablespoons (preferably sesame oil)
Onions -""", 2 * n, """medium-sized, finely chopped
Tomatoes -""", 2 * n, """medium-sized, finely chopped
Ginger Garlic Paste -""", 2 * n, """teaspoons
Turmeric Powder -""", 0.5 * n, """teaspoon
Salt - to taste
Curry Leaves - a sprig""")

    print(blue('\nCHETTINAD MASALA INGREDIENTS\n', ['bold', 'underlined']))
    print("""Dry Red Chilies -""", 7 * n, """(adjust to taste)
Coriander Seeds -""", 2 * n, """tablespoons
Cumin Seeds -""", 1 * n, """teaspoon
Fennel Seeds -""", 1 * n, """teaspoon
Black Peppercorns -""", 1 * n, """teaspoon
Cloves - """, 4 * n, """
Cinnamon Stick -""", 1 * n, """-inch piece
Green Cardamoms - """, 3 * n, """
Poppy Seeds -""", 1 * n, """teaspoon
Grated Coconut -""", (1/4) * n, """cup""")

    print(yellow('\nRECIPE\n', ['bold', 'underlined']))

    print("""Prepare the Chettinad Masala:
Dry roast the dry red chilies, coriander seeds, cumin seeds, fennel seeds, black peppercorns, cloves, cinnamon stick, and green cardamoms in a pan until aromatic.
Add the poppy seeds and grated coconut, and roast until the coconut turns golden brown.
Let the mixture cool, then grind it into a fine powder.

Cooking the Chicken:
Heat oil in a large pan or kadai.
Add the finely chopped onions and saut√© until they turn golden brown.
Add the ginger-garlic paste and saut√© for a couple of minutes until the raw smell disappears.
Add the finely chopped tomatoes and cook until they turn soft and oil starts to separate from the mixture.
Add the turmeric powder and salt, and mix well.
Add the chicken pieces and saut√© until they are well coated with the masala.
Add the prepared Chettinad masala powder and mix well.
Add about 1 cup of water, cover, and let it cook on medium heat until the chicken is fully cooked and tender, stirring occasionally.
Once the chicken is cooked, add the curry leaves and simmer for a few more minutes.
Adjust the consistency of the gravy as per your preference. If you want a thicker gravy, cook it a bit longer uncovered.

Garnish and Serve:
Garnish with fresh coriander leaves if desired.
Serve hot with rice, roti, or dosa.""")

